//鼠标放在div上显示商品介绍
/*商品推荐横排两行四列上显示商品介绍*/
$(".shows-product1").hover(function(){
	$(this).find(".shows-textbox1").show();
	},function(){
		$(this).find(".shows-textbox1").hide();
		});

/*左一右四上显示商品介绍*/
